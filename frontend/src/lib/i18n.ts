/** All translation keys (for type-safe t() in components). */
export type TranslationKey = keyof typeof translations.tr;

class TranslationEngine {
  private currentLang: Language = (localStorage.getItem('stellarsplit_lang') as Language) || 'tr';

  get lang() { return this.currentLang; }

  setLang(l: Language) {
    this.currentLang = l;
    localStorage.setItem('stellarsplit_lang', l);
    window.dispatchEvent(new CustomEvent('stellarsplit:lang-updated'));
  }

  t(key: TranslationKey): string {
    return translations[this.currentLang][key] || key;
  }
}

export const i18n = new TranslationEngine();

export function useI18n() {
  const [, setTick] = useState(0);

  useEffect(() => {
    const update = () => setTick(t => t + 1);
    window.addEventListener('stellarsplit:lang-updated', update);
    return () => window.removeEventListener('stellarsplit:lang-updated', update);
  }, []);

  return {
    t: (key: TranslationKey) => i18n.t(key),
    lang: i18n.lang,
    setLang: (l: Language) => i18n.setLang(l)
  };
}
